// C program to find the size of int, char,
// float and double data types

#include <stdio.h>

int main()
{
    int x;
    char y;
    float z;
    double k;
    printf("Size of int    = %d\n",sizeof(x));
    printf("Size of char   = %d\n",sizeof(y));
    printf("Size of float  = %d\n",sizeof(z));
    printf("Size of double = %d\n",sizeof(k));


    return 0;
}